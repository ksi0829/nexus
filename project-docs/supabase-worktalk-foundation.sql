create or replace function public.worktalk_is_privileged()
returns boolean
language sql
stable
security definer
set search_path = public
as $function$
  select exists (
    select 1
    from public.profiles profile
    where profile.id = auth.uid()
      and profile.role in ('admin', 'executive')
  );
$function$;

create or replace function public.worktalk_is_room_member(target_room_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $function$
  select exists (
    select 1
    from public.worktalk_room_members member
    where member.room_id = target_room_id
      and member.user_id = auth.uid()
      and member.left_at is null
  );
$function$;

create or replace function public.worktalk_can_view_room(target_room_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $function$
  select public.worktalk_is_privileged()
    or public.worktalk_is_room_member(target_room_id);
$function$;

create or replace function public.worktalk_can_manage_room(target_room_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $function$
  select public.worktalk_is_privileged()
    or exists (
      select 1
      from public.worktalk_rooms room
      join public.worktalk_room_members member
        on member.room_id = room.id
      where room.id = target_room_id
        and room.room_type = 'group'
        and room.created_by = auth.uid()
        and member.user_id = auth.uid()
        and member.left_at is null
    );
$function$;

create or replace function public.worktalk_create_direct_room(target_user_id uuid)
returns bigint
language plpgsql
security definer
set search_path = public
as $function$
declare
  existing_room_id bigint;
  new_room_id bigint;
  current_profile public.profiles%rowtype;
  target_profile public.profiles%rowtype;
begin
  if target_user_id is null or target_user_id = auth.uid() then
    raise exception 'A different user is required.';
  end if;

  select * into current_profile from public.profiles where id = auth.uid();
  select * into target_profile from public.profiles where id = target_user_id;

  if current_profile.id is null or target_profile.id is null then
    raise exception 'Profile was not found.';
  end if;

  select room.id
  into existing_room_id
  from public.worktalk_rooms room
  where room.room_type = 'direct'
    and room.is_archived = false
    and (
      select count(*)
      from public.worktalk_room_members member
      where member.room_id = room.id
        and member.left_at is null
    ) = 2
    and exists (
      select 1 from public.worktalk_room_members member
      where member.room_id = room.id and member.user_id = auth.uid() and member.left_at is null
    )
    and exists (
      select 1 from public.worktalk_room_members member
      where member.room_id = room.id and member.user_id = target_user_id and member.left_at is null
    )
  limit 1;

  if existing_room_id is not null then
    return existing_room_id;
  end if;

  insert into public.worktalk_rooms (room_type, title, created_by)
  values ('direct', '', auth.uid())
  returning id into new_room_id;

  insert into public.worktalk_room_members (room_id, user_id, member_role)
  values
    (new_room_id, auth.uid(), 'owner'),
    (new_room_id, target_user_id, 'member');

  return new_room_id;
end;
$function$;

create or replace function public.worktalk_create_group_room(
  room_title text,
  member_ids uuid[]
)
returns bigint
language plpgsql
security definer
set search_path = public
as $function$
declare
  new_room_id bigint;
begin
  if nullif(btrim(room_title), '') is null then
    raise exception 'Room title is required.';
  end if;

  if member_ids is null or cardinality(member_ids) < 1 then
    raise exception 'At least one member is required.';
  end if;

  insert into public.worktalk_rooms (room_type, title, created_by)
  values ('group', btrim(room_title), auth.uid())
  returning id into new_room_id;

  insert into public.worktalk_room_members (room_id, user_id, member_role)
  values (new_room_id, auth.uid(), 'owner');

  insert into public.worktalk_room_members (room_id, user_id, member_role)
  select new_room_id, member_id, 'member'
  from unnest(member_ids) member_id
  where member_id <> auth.uid()
    and exists (select 1 from public.profiles profile where profile.id = member_id)
  on conflict (room_id, user_id) do nothing;

  return new_room_id;
end;
$function$;

create or replace function public.worktalk_send_message(
  target_room_id bigint,
  message_body text
)
returns bigint
language plpgsql
security definer
set search_path = public
as $function$
declare
  profile_row public.profiles%rowtype;
  new_message_id bigint;
begin
  if not public.worktalk_is_room_member(target_room_id) then
    raise exception 'Only room members may send messages.';
  end if;

  if nullif(btrim(message_body), '') is null then
    raise exception 'Message body is required.';
  end if;

  select * into profile_row from public.profiles where id = auth.uid();

  insert into public.worktalk_messages (
    room_id,
    sender_id,
    sender_name,
    sender_team,
    body
  )
  values (
    target_room_id,
    auth.uid(),
    coalesce(profile_row.name, ''),
    profile_row.team,
    btrim(message_body)
  )
  returning id into new_message_id;

  update public.worktalk_rooms
  set last_message_at = now(), updated_at = now()
  where id = target_room_id;

  update public.worktalk_room_members
  set last_read_message_id = new_message_id, last_read_at = now()
  where room_id = target_room_id and user_id = auth.uid();

  return new_message_id;
end;
$function$;

create or replace function public.worktalk_mark_room_read(
  target_room_id bigint,
  target_message_id bigint default null
)
returns void
language plpgsql
security definer
set search_path = public
as $function$
declare
  resolved_message_id bigint;
begin
  if not public.worktalk_is_room_member(target_room_id) then
    raise exception 'Only room members may mark messages as read.';
  end if;

  if target_message_id is null then
    select max(id) into resolved_message_id
    from public.worktalk_messages
    where room_id = target_room_id;
  else
    select id into resolved_message_id
    from public.worktalk_messages
    where room_id = target_room_id and id = target_message_id;
  end if;

  update public.worktalk_room_members
  set last_read_message_id = resolved_message_id, last_read_at = now()
  where room_id = target_room_id and user_id = auth.uid();
end;
$function$;

create or replace function public.worktalk_set_room_notifications(
  target_room_id bigint,
  enabled boolean
)
returns void
language sql
security definer
set search_path = public
as $function$
  update public.worktalk_room_members
  set notifications_enabled = enabled
  where room_id = target_room_id
    and user_id = auth.uid()
    and left_at is null;
$function$;

create or replace function public.worktalk_sync_fixed_rooms()
returns void
language plpgsql
security definer
set search_path = public
as $function$
begin
  if not public.worktalk_is_privileged() then
    raise exception 'Administrator or executive access required.';
  end if;

  -- NEXUS 기본 고정방은 전 임직원 공용 방 3개와 아이디어 소통방만 유지한다.
  -- profiles.team 전체를 기준으로 부서별 방을 자동 생성하지 않는다.
  insert into public.worktalk_rooms (
    room_type,
    title,
    team_key,
    is_fixed,
    created_by
  )
  values
    ('team', '관리/영업-전임직원', '__company_admin_sales__', true, auth.uid()),
    ('team', '생산-전임직원', '__company_production__', true, auth.uid()),
    ('team', '개발-전임직원', '__company_development__', true, auth.uid()),
    ('idea', '아이디어 소통방', '__all__', true, auth.uid())
  on conflict (room_type, team_key) where team_key is not null
  do update set
    title = excluded.title,
    is_fixed = true,
    is_archived = false,
    archived_at = null,
    updated_at = now();

  insert into public.worktalk_room_members (room_id, user_id, member_role)
  select room.id, profile.id, 'member'
  from public.worktalk_rooms room
  cross join public.profiles profile
  where room.team_key in (
    '__company_admin_sales__',
    '__company_production__',
    '__company_development__',
    '__all__'
  )
  on conflict (room_id, user_id)
  do update set left_at = null;
end;
$function$;


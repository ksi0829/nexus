create table if not exists public.worktalk_rooms (
  id bigint generated by default as identity primary key,
  room_type text not null check (room_type in ('direct', 'group', 'team', 'idea', 'approval')),
  title text not null default '',
  team_key text,
  created_by uuid references public.profiles(id) on delete set null default auth.uid(),
  is_fixed boolean not null default false,
  is_archived boolean not null default false,
  archived_at timestamptz,
  last_message_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.worktalk_room_members (
  room_id bigint not null references public.worktalk_rooms(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete restrict,
  member_role text not null default 'member'
    check (member_role in ('owner', 'member', 'viewer')),
  notifications_enabled boolean not null default true,
  is_pinned boolean not null default false,
  sort_order integer not null default 0,
  joined_at timestamptz not null default now(),
  left_at timestamptz,
  last_read_message_id bigint,
  last_read_at timestamptz,
  primary key (room_id, user_id)
);

create table if not exists public.worktalk_messages (
  id bigint generated by default as identity primary key,
  room_id bigint not null references public.worktalk_rooms(id) on delete cascade,
  sender_id uuid references public.profiles(id) on delete set null default auth.uid(),
  sender_name text not null,
  sender_team text,
  message_type text not null default 'text'
    check (message_type in ('text', 'file', 'system', 'document')),
  body text not null default '',
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

alter table public.worktalk_room_members
  drop constraint if exists worktalk_room_members_last_read_message_id_fkey;

alter table public.worktalk_room_members
  add constraint worktalk_room_members_last_read_message_id_fkey
  foreign key (last_read_message_id)
  references public.worktalk_messages(id)
  on delete set null;

create table if not exists public.worktalk_files (
  id bigint generated by default as identity primary key,
  room_id bigint not null references public.worktalk_rooms(id) on delete cascade,
  message_id bigint not null references public.worktalk_messages(id) on delete cascade,
  storage_path text not null unique,
  original_name text not null,
  mime_type text,
  size_bytes bigint not null check (size_bytes > 0 and size_bytes <= 31457280),
  uploaded_by uuid references public.profiles(id) on delete set null default auth.uid(),
  created_at timestamptz not null default now()
);

create table if not exists public.worktalk_push_subscriptions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade default auth.uid(),
  endpoint text not null,
  p256dh text not null,
  auth_key text not null,
  device_label text,
  user_agent text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (user_id, endpoint)
);

